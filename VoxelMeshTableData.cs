    {
        int index = voxelIndex + 
            voxelIndex / WorldSettings.Instance.NumVoxelsPerAxis +
            (voxelIndex / WorldSettings.Instance.NumVoxelsPerAxisSqr * (WorldSettings.Instance.NumVoxelsPerAxis + 1));
        int[] triangles = triangleTable[b];
        int[] rtn = new int[triangles.Length];
        for(int i = 0; i < rtn.Length; i++)
        {
            rtn[i] = WorldSettings.Instance.VoxelCornersTable[triangles[i]] + index;
        }
        return rtn;
    }

